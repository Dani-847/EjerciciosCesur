public class Persona {
    int edad;
    int peso;

    public Persona(int edad, int peso) {
        this.edad = edad;
        this.peso = peso;
    }

    public int getEdad() { return edad; }
    public int getPeso() {
        return peso;
    }
    public void setEdad(int edad) {
        this.edad = edad;
    }
    public void setPeso(int peso) {
        this.peso = peso;
    }
}
